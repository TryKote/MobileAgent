package com.trykote.editor.build;

import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.IOException;
import java.nio.file.*;
import java.util.*;

/**
 * Generates ScreenDef.java from config.json.
 * Java replacement for: python3 tools/cfg_tool.py --gen-screens
 */
public class ScreenDefGenerator {

    static final int OBJECT_POOL_SIZE = 1406;
    static final int INT_POOL_HEADER_SIZE = 172;

    public static void generate(Path inputDir, Path outputJava) throws IOException {
        ObjectMapper mapper = new ObjectMapper();
        JsonNode config = mapper.readTree(inputDir.resolve("config.json").toFile());

        // Inject palette into VCARD_ACTIONS trailingData for offset computation
        int paletteSize = injectPaletteSize(inputDir);

        JsonNode screens = config.get("screens");
        if (screens == null || !screens.isArray()) {
            throw new IOException("No 'screens' array in config.json");
        }

        var lines = new ArrayList<String>();
        lines.add("package com.trykote.mobileagent.core;");
        lines.add("");
        lines.add("/**");
        lines.add(" * IntPool offsets for screen definitions used by ScreenManager.createScreen().");
        lines.add(" * Generated by editor --gen-screens. Do not edit manually.");
        lines.add(" */");
        lines.add("public final class ScreenDef {");
        lines.add("    private ScreenDef() {}");
        lines.add("");

        int offset = OBJECT_POOL_SIZE + INT_POOL_HEADER_SIZE;
        int count = 0;

        for (JsonNode screen : screens) {
            String name = screen.path("name").asText();
            lines.add("    public static final int " + name + " = " + offset + ";");

            int itemsSize = 0;
            JsonNode items = screen.get("items");
            if (items != null) {
                for (JsonNode item : items) {
                    itemsSize += computeItemSize(item);
                }
            }

            int trailingSize;
            if ("VCARD_ACTIONS".equals(name) && paletteSize > 0) {
                trailingSize = paletteSize;
            } else {
                JsonNode trailing = screen.get("trailingData");
                trailingSize = (trailing != null && trailing.isArray()) ? trailing.size() : 0;
            }

            offset += 10 + itemsSize + trailingSize;
            count++;
        }

        lines.add("}");
        lines.add("");

        Files.createDirectories(outputJava.getParent());
        Files.writeString(outputJava, String.join("\n", lines));
        System.out.println("Wrote " + outputJava + " (" + count + " screen definitions)");
    }

    /**
     * Compute compiled size (in ints) of one screen item.
     * Must match cfg_tool.py _compile_item() sizes exactly.
     */
    static int computeItemSize(JsonNode item) {
        String type = item.path("type").asText();
        return switch (type) {
            case "action" -> item.has("extra") ? 5 : 4;
            case "separator" -> 3;
            case "checkbox" -> 3;
            case "dropdown" -> 4;
            case "text_separator", "label_separator" -> 2;
            case "text_input" -> item.path("validation").asInt() == 2 ? 9 : 6;
            case "conditional_if", "conditional_unless" -> 5;
            case "login" -> 3;
            case "password" -> 2;
            case "image" -> 2;
            case "redirect" -> 2;
            default -> 2;
        };
    }

    private static int injectPaletteSize(Path inputDir) {
        // Palette is always 22 roles × 8 themes = 176 ints when present
        Path palettePath = inputDir.resolve("palette.json");
        return Files.exists(palettePath) ? 176 : 0;
    }
}

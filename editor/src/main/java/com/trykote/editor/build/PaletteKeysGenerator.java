package com.trykote.editor.build;

import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.IOException;
import java.nio.file.*;
import java.util.*;

/**
 * Generates PaletteKeys.java from palette.json + config.json.
 * Java replacement for: python3 tools/cfg_tool.py --gen-palette
 */
public class PaletteKeysGenerator {

    private static final int PALETTE_INDEXED_COUNT = 16;
    private static final int PALETTE_THEME_COUNT = 8;

    private static final String[] PALETTE_ROLES = {
        "text", "background", "background_alt", "link",
        "online", "away", "offline", "accent",
        "selection_bg", "selection_text", "separator", "disabled",
        "border", "scrollbar", "popup_bg", "popup_text",
        "gradient_start", "map_fill", "map_border", "map_bg",
        "map_pulse", "gradient_end"
    };

    public static void generate(Path inputDir, Path outputJava) throws IOException {
        ObjectMapper mapper = new ObjectMapper();
        JsonNode config = mapper.readTree(inputDir.resolve("config.json").toFile());

        // Inject palette for offset computation
        CfgSerializer.injectPalette(config, inputDir);

        // Compute COLORS_BASE: VCARD_ACTIONS trailingData offset
        int colorsBase = computeVcardTrailingOffset(config);

        var lines = new ArrayList<String>();
        lines.add("package com.trykote.mobileagent.core;");
        lines.add("");
        lines.add("/**");
        lines.add(" * Generated by editor --gen-palette. Do not edit manually.");
        lines.add(" */");
        lines.add("public final class PaletteKeys {");
        lines.add("    private PaletteKeys() {}");
        lines.add("");
        lines.add("    public static final int COLORS_BASE = " + colorsBase + ";");

        // Extended roles: roles[16:] get named constants
        int extBase = colorsBase + PALETTE_INDEXED_COUNT * PALETTE_THEME_COUNT;
        for (int i = PALETTE_INDEXED_COUNT; i < PALETTE_ROLES.length; i++) {
            String name = PALETTE_ROLES[i].toUpperCase();
            lines.add("    public static final int " + name + " = " + extBase + ";");
            extBase += PALETTE_THEME_COUNT;
        }

        lines.add("}");
        lines.add("");

        Files.createDirectories(outputJava.getParent());
        Files.writeString(outputJava, String.join("\n", lines));
        System.out.println("Wrote " + outputJava);
    }

    private static int computeVcardTrailingOffset(JsonNode config) {
        int offset = ScreenDefGenerator.OBJECT_POOL_SIZE + ScreenDefGenerator.INT_POOL_HEADER_SIZE;
        JsonNode screens = config.get("screens");
        if (screens == null) return offset;

        for (JsonNode screen : screens) {
            int itemsSize = 0;
            JsonNode items = screen.get("items");
            if (items != null) {
                for (JsonNode item : items) {
                    itemsSize += ScreenDefGenerator.computeItemSize(item);
                }
            }
            if ("VCARD_ACTIONS".equals(screen.path("name").asText())) {
                return offset + 10 + itemsSize; // header + items, before trailingData
            }
            JsonNode trailing = screen.get("trailingData");
            int trailingSize = (trailing != null && trailing.isArray()) ? trailing.size() : 0;
            offset += 10 + itemsSize + trailingSize;
        }
        return offset;
    }
}
